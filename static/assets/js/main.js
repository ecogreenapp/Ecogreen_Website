$(document).ready(function () {
  // Init
  $(".image-section").hide();
  $(".loader").hide();
  $("#result").hide();

  // Upload Preview
  function readURL(input) {
    if (input.files && input.files[0]) {
      var reader = new FileReader();
      reader.onload = function (e) {
        $("#imagePreview").css(
          "background-image",
          "url(" + e.target.result + ")"
        );
        $("#imagePreview").hide();
        $("#imagePreview").fadeIn(650);
      };
      reader.readAsDataURL(input.files[0]);
    }
  }
  $("#imageUpload").change(function () {
    $(".image-section").show();
    $("#btn-predict").show();
    $("#result").text("");
    $("#result").hide();
    readURL(this);
  });

  // Predict
  $("#btn-predict").click(function () {
    var form_data = new FormData($("#upload-file")[0]);

    // Show loading animation
    $(this).hide();
    $(".loader").show();

    // Make prediction by calling api /deteksi
    $.ajax({
      type: "POST",
      url: "/deteksi",
      data: form_data,
      contentType: false,
      cache: false,
      processData: false,
      async: true,
      success: function (data) {
        console.log("Received data:", data); // Log the entire data object

        // Get and display the result
        $(".loader").hide();
        $("#result").fadeIn(600);

        // Display prediction result and accuracy
        $("#result").text(
          "Hasil: " +
            data.prediction +
            " | Akurasi: " +
            data.accuracy.toFixed(2) +
            "%"
        );

        // Add condition to check if the result is organic or non-organic
        if (data.prediction.toLowerCase() === "sampah organik") {
          $("#result").append("<p>Ini adalah sampah organik.</p>");
        } else {
          $("#result").append("<p>Ini adalah sampah non-organik.</p>");
        }

        console.log("Success!");
      },
      error: function (error) {
        // Handle the error, if any
        console.log("Error:", error.responseText);
      },
    });
  });
});

